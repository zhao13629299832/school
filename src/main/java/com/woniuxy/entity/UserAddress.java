package com.woniuxy.entity;

public class UserAddress {
    private String userAddressId;

    private String userPhone;

    private String userAddressProvince;

    private String userAddressCity;

    private String userAddressArea;

    private String userAddressSchool;

    private String userAddressName;

    private Boolean userAddressIsdelete;

    public String getUserAddressId() {
        return userAddressId;
    }

    public void setUserAddressId(String userAddressId) {
        this.userAddressId = userAddressId;
    }

    public String getUserPhone() {
        return userPhone;
    }

    public void setUserPhone(String userPhone) {
        this.userPhone = userPhone;
    }

    public String getUserAddressProvince() {
        return userAddressProvince;
    }

    public void setUserAddressProvince(String userAddressProvince) {
        this.userAddressProvince = userAddressProvince;
    }

    public String getUserAddressCity() {
        return userAddressCity;
    }

    public void setUserAddressCity(String userAddressCity) {
        this.userAddressCity = userAddressCity;
    }

    public String getUserAddressArea() {
        return userAddressArea;
    }

    public void setUserAddressArea(String userAddressArea) {
        this.userAddressArea = userAddressArea;
    }

    public String getUserAddressSchool() {
        return userAddressSchool;
    }

    public void setUserAddressSchool(String userAddressSchool) {
        this.userAddressSchool = userAddressSchool;
    }

    public String getUserAddressName() {
        return userAddressName;
    }

    public void setUserAddressName(String userAddressName) {
        this.userAddressName = userAddressName;
    }

    public Boolean getUserAddressIsdelete() {
        return userAddressIsdelete;
    }

    public void setUserAddressIsdelete(Boolean userAddressIsdelete) {
        this.userAddressIsdelete = userAddressIsdelete;
    }
}